CREATE TABLE ERPS.ACTIVITY_LOG
(
  LOG_ID      VARCHAR2(10)       PRIMARY KEY,
  USER_ID     VARCHAR2(20)       NOT NULL,
  LOG_DATE    DATE               NOT NULL,
  SKU_CODE    VARCHAR2(18),
  SAP_ID  	  VARCHAR2(18),
  ROOT_ID     VARCHAR2(32),
  CHILD1_ID   VARCHAR2(32),
  CHILD2_ID   VARCHAR2(32),
  ATR_TYPE    VARCHAR2(1),
  ATR_NAME    VARCHAR2(40),
  NUTRITION_TYPE VARCHAR2(255),
  NUTRITION_PRIORITY NUMBER(2),
  NUTRITIONINFO_TYPE VARCHAR2(4),
  COLUMN_NAME  VARCHAR(40),
  OLD_VALUE   VARCHAR2(4000),
  NEW_VALUE   VARCHAR2(4000)
);

GRANT SELECT ON ERPS.ACTIVITY_LOG TO FDSTORE_PRDA
GRANT ALL ON ERPS.ACTIVITY_LOG TO fdstore;
GRANT READ ON ERPS.ACTIVITY_LOG TO APPDEV;

CREATE SEQUENCE ERPS.ACTIVITYLOG_SEQ
  START WITH 1
  MAXVALUE 999999999999999999999999999
  MINVALUE 1
  NOCYCLE
  NOCACHE
  NOORDER;


GRANT SELECT ON ERPS.ACTIVITYLOG_SEQ TO FDSTORE_PRDA; 
GRANT ALTER, SELECT ON ERPS.ACTIVITYLOG_SEQ TO fdstore; 
GRANT SELECT ON ERPS.ACTIVITYLOG_SEQ TO APPDEV; 

