-- Create CASE_CARTONS table
--
CREATE TABLE "CUST"."CASE_CARTONS" (
  "CASE_ID" VARCHAR2(16 BYTE) NOT NULL ENABLE, 
  "CARTON_NUMBER" VARCHAR2(20 BYTE) NOT NULL ENABLE, 
  CONSTRAINT "CASE_CARTONS_PK" PRIMARY KEY ("CASE_ID", "CARTON_NUMBER"),
  CONSTRAINT "CASE_CARTONS_CASE_FK1" FOREIGN KEY ("CASE_ID")
  REFERENCES "CUST"."CASE" ("ID")
);

