-- DYF / Purchase History
INSERT INTO CUST.SSM_PURCHASE_HISTORY
SELECT CUSTOMER_ID, PARENT_CONTENT_ID AS PRODUCT_ID, COUNT(PARENT_CONTENT_ID) AS SCORE
FROM CUST.SSA_ORDERLINE OL, CUST.SSA_SALE SAL, CUST.SSA_STORE ST
WHERE
  OL.SALE_ID = SAL.SALE_ID
AND
  ST.CONTENT_TYPE = 'Sku'
AND
  ST.CONTENT_ID = OL.SKU_CODE
GROUP BY CUSTOMER_ID, PARENT_CONTENT_ID;

