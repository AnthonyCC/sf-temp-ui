(
  COUNTRY_CODE  VARCHAR2(2 BYTE)                NOT NULL,
  CODE          VARCHAR2(6 BYTE)                NOT NULL,
  NAME          VARCHAR2(48 BYTE),
  ID            VARCHAR2(16 BYTE)               NOT NULL
);

CREATE UNIQUE INDEX CUST.REGION_PK ON CUST.REGION
(COUNTRY_CODE, CODE)
);

ALTER TABLE CUST.REGION ADD (
