CREATE TABLE CUST.COUNTRY
(
  ID               VARCHAR2(16 BYTE),
  CODE             VARCHAR2(2 BYTE)             NOT NULL,
  NAME             VARCHAR2(48 BYTE)            NOT NULL,
  ZIP_CHECK_REGEX  VARCHAR2(512 BYTE)
);

ALTER TABLE CUST.COUNTRY ADD (
  PRIMARY KEY
 (ID)
 );
 
