
CREATE TABLE CUST.SALE_MATERIAL
(
  SALE_ID           VARCHAR2(16 BYTE),
  MATERIAL_SAP_ID   VARCHAR2(18 BYTE),
  INSERT_TIMESTAMP  DATE,
  REQUESTED_DATE    DATE,
  QUANTITY          NUMBER
);

CREATE INDEX CUST.IDX_MATERIAL_SAP_ID ON CUST.SALE_MATERIAL
(MATERIAL_SAP_ID);

CREATE INDEX CUST.IDX_REQUESTED_DATE ON CUST.SALE_MATERIAL
(REQUESTED_DATE);

CREATE INDEX CUST.IDX_SALE_ID ON CUST.SALE_MATERIAL
(SALE_ID);