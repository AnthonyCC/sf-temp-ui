--
-- PROMOTIONLINE  (Table) 
--
--  Dependencies: 
--   SALESACTION (Table)
--
CREATE TABLE CUST.DISCOUNTLINE
(
  ID                  VARCHAR2(16 BYTE)         NOT NULL,
  SALESACTION_ID      VARCHAR2(16 BYTE)         NOT NULL,
  PROMOTION_TYPE      NUMBER(10),
  PROMOTION_AMT       NUMBER(10,2),
  PROMOTION_CAMPAIGN  VARCHAR2(16 BYTE)
)
LOGGING 
NOCACHE
NOPARALLEL
MONITORING;


--
-- PK_DISCOUNTLINE  (Index) 
--
--  Dependencies: 
--   DISCOUNTLINE (Table)
--
CREATE UNIQUE INDEX CUST.PK_DISCOUNTLINE ON CUST.DISCOUNTLINE
(ID)
LOGGING
NOPARALLEL;


--
-- DSCTLIN_SACT_IDX  (Index) 
