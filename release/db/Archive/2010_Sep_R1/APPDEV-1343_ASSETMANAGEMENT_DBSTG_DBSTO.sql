CREATE SEQUENCE TRANSP.ASSETSEQ
  START WITH 1
  MAXVALUE 999999999999999999999999999
  MINVALUE 1
  NOCYCLE
  CACHE 20
  NOORDER;
  
CREATE TABLE TRANSP.ASSET_TYPE
(
  CODE         VARCHAR2(40 BYTE)                 NOT NULL,
  DESCRIPTION  VARCHAR2(40 BYTE)                 NOT NULL
);

ALTER TABLE TRANSP.ASSET_TYPE ADD (PRIMARY KEY (CODE));


CREATE TABLE TRANSP.ASSET_ATTRIBUTETYPE
(
  CODE         VARCHAR2(40 BYTE)                  NOT NULL,
  DESCRIPTION  VARCHAR2(40 BYTE)                 NOT NULL,
  DATA_TYPE    VARCHAR2(40 BYTE)                  NOT NULL
);

ALTER TABLE TRANSP.ASSET_ATTRIBUTETYPE ADD (PRIMARY KEY (CODE));

CREATE TABLE TRANSP.ASSET
(
  ASSET_ID                 VARCHAR2(40 BYTE)          NOT NULL,
  ASSET_NO   		   VARCHAR2(40 BYTE)          NOT NULL,
  ASSET_TYPE               VARCHAR2(40 BYTE)         NOT NULL,
  ASSET_DESCRIPTION        VARCHAR2(256 BYTE),
  ASSET_STATUS             VARCHAR2(40 BYTE)
);

ALTER TABLE TRANSP.ASSET ADD (PRIMARY KEY (ASSET_ID));
ALTER TABLE TRANSP.ASSET ADD (UNIQUE (ASSET_NO, ASSET_TYPE));
ALTER TABLE TRANSP.ASSET ADD (FOREIGN KEY (ASSET_TYPE) REFERENCES TRANSP.ASSET_TYPE (CODE));

CREATE INDEX TRANSP.IDX01_ASSET_TYPE ON TRANSP.ASSET(ASSET_TYPE);


CREATE TABLE TRANSP.ASSET_ATTRIBUTE
(
  ASSET_ID         VARCHAR2(40 BYTE)            NOT NULL,
  ATTRIBUTE_TYPE   VARCHAR2(40 BYTE)             NOT NULL,
  ATTRIBUTE_VALUE  VARCHAR2(40 BYTE)             NOT NULL
);

ALTER TABLE TRANSP.ASSET_ATTRIBUTE ADD (PRIMARY KEY (ASSET_ID, ATTRIBUTE_TYPE));
ALTER TABLE TRANSP.ASSET_ATTRIBUTE ADD (FOREIGN KEY (ASSET_ID) REFERENCES TRANSP.ASSET (ASSET_ID));
ALTER TABLE TRANSP.ASSET_ATTRIBUTE ADD (FOREIGN KEY (ATTRIBUTE_TYPE) REFERENCES TRANSP.ASSET_ATTRIBUTETYPE (CODE));

CREATE INDEX TRANSP.IDX01_ASSET_ATTRIBUTE ON TRANSP.ASSET_ATTRIBUTE(ASSET_ID);


