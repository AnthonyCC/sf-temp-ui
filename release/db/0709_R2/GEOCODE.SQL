CREATE TABLE MRKTING.COMPETITIVE_LOC
(
  COMPETITOR_NAME   VARCHAR2(128 BYTE),
  ADDRESS1          VARCHAR2(55 BYTE),
  ADDRESS2          VARCHAR2(55 BYTE),
  APARTMENT         VARCHAR2(55 BYTE),
  CITY              VARCHAR2(55 BYTE),
  STATE             VARCHAR2(2 BYTE),
  ZIP               VARCHAR2(10 BYTE),
  COUNTRY           VARCHAR2(10 BYTE),
  SCRUBBED_ADDRESS  VARCHAR2(55 BYTE),
  COMPETITOR_TYPE   VARCHAR2(32 BYTE),
  DATE_CREATED      DATE,
  STATUS            VARCHAR2(16 BYTE),
  MI_STYLE          VARCHAR2(254 BYTE),
  MI_PRINX          NUMBER(10) PRIMARY KEY,
  GEOLOC            MDSYS.SDO_GEOMETRY
);



CREATE TABLE MRKTING.CUSTOMER_ADDR
(
  MI_PRINX          NUMBER(12) PRIMARY KEY,
  ADDRESS1          VARCHAR2(55 BYTE)           NOT NULL,
  ADDRESS2          VARCHAR2(55 BYTE),
  APARTMENT         VARCHAR2(55 BYTE),
  CITY              VARCHAR2(55 BYTE)           NOT NULL,
  STATE             VARCHAR2(2 BYTE)            NOT NULL,
  ZIP               VARCHAR2(10 BYTE)           NOT NULL,
  COUNTRY           VARCHAR2(10 BYTE)           NOT NULL,
  SCRUBBED_ADDRESS  VARCHAR2(55 BYTE),
  GEOLOC            MDSYS.SDO_GEOMETRY,
  DATE_CREATED      DATE
);
